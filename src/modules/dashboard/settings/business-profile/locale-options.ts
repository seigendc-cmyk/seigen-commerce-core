export type LocaleOption = { value: string; label: string };

export type LocaleOptionGroup = { group: string; options: LocaleOption[] };

function sortByLabel(opts: LocaleOption[]): LocaleOption[] {
  return [...opts].sort((a, b) => a.label.localeCompare(b.label, "en"));
}

